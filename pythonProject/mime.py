import random

# Налаштування
назва = "Ти легенда?"
відповіді = ["Так", "Ні"]
переміщення = 50
повідомлення_про_смерть = "я вже дуже старий для всього цього дерьма  навіть кс 2 не тяну твій пк."
повідомлення_про_перемогу = " 38 ударів ножом ви жестоко покончили з кені так?"
повідомлення_про_пакой = "я легенда ожна на пакой, Серьога."


# Функція для генерації таблички
def друкувати_табличку(назва, відповіді):
    print("=" * len(назва))
    print(назва)
    print("=" * len(назва))
    for відповідь in відповіді:
        print(f"[ ] {відповідь}")


# Функція для переміщення кнопки
def перемістити_кнопку(переміщення):
    global відповіді

    # Випадкове значення для переміщення
    зсув = random.randint(-переміщення, переміщення)

    # Оновлення індексу кнопки "Так"
    індекс_так = відповіді.index("Так")
    новий_індекс_так = (індекс_так + зсув) % len(відповіді)

    # Переміщення кнопки "Так"
    відповіді[індекс_так], відповіді[новий_індекс_так] = відповіді[новий_індекс_так], відповіді[індекс_так]


# Функція для друку квадратиків
def друкувати_квадратики(відповіді):
    for i, відповідь in enumerate(відповіді):
        print(f"[{i + 1}] {відповідь}", end=" ")
    print()


# Ігровий цикл
while True:
    друкувати_квадратики(відповіді)

    # Введення користувача
    відповідь = input("Введіть номер відповіді: ").strip()

    # Перевірка відповіді
    try:
        відповідь = int(відповідь) - 1
        if not 0 <= відповідь < len(відповіді):
            raise ValueError
    except ValueError:
        print("Невідомий номер. Спробуйте ще раз.")
        continue

    # Реакція на відповідь
    if відповіді[відповідь] == "Так":
        print(повідомлення_про_смерть)
        break
    else:
        print(повідомлення_про_перемогу)
        print(повідомлення_про_пакой)
        break

